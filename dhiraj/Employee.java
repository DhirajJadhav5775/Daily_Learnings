package com.dhiraj;

public class Employee
{
    private int EmpID;
    private float Salary;

    public int getEmpID()
    {
        return EmpID;
    }
    public void setEmpID(int ID)
    {
        EmpID = ID;
    }
    public float getSalary() {
        return Salary;
    }
    public void setSalary(float salary) {
        Salary = salary;
    }
}
