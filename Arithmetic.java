package Marvellous;
public class Arithmetic
{
    public int Value1;
    public int Value2;

    Arithmetic(int A, int B)
    {
        Value1 = A;
        Value2 = B;
    }

    int Addition()
    {
        return Value1 + Value2;
    }

    int Substraction()
    {
        return Value1 - Value2;
    }
}